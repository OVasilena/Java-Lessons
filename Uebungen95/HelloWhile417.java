public class HelloWhile417 {
    public static void main(String[] args) {
        int i = 20;

        while (i > 0){
            System.out.print(i + "\t");
            i -= 2;
        }
        System.out.println();
        for (int k=20; k > 0; k -=2){
            System.out.print(k + "\t");

        }

    }
}
